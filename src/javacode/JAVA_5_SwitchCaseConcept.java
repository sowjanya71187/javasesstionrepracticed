package javacode;

public class JAVA_5_SwitchCaseConcept {

	public static void main(String[] args) {
		
		String browser = "chrome";
		switch (browser) {
		case "chrome":
			System.out.println("chrome");
			
			break;
        case "firefox":
        	System.out.println("firefox");
			
			break;
        case "opera":
        	System.out.println("opera");
	
	break;

		default:
			break;
		}
		

	}

	

}
